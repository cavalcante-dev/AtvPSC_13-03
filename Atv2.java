//while - Contagem de 1 a 10

public class Atv2 {
    public static void main(String[] args) {
        
        int num1 = 1;

        System.out.println("Contagem regressiva: ");

        while (num1 <= 10) {
            
           System.out.println("Contagem - " + num1);
           num1++; 

        }

    }
}
